function checkCashRegister(price, cash, cid) {
    let original_change = cash - price;
    let change_array = [];
    let quotient;
    const money_value = {
        "PENNY": 0.01,
        "NICKEL": 0.05,
        "DIME": 0.1,
        "QUARTER": 0.25,
        "ONE": 1,
        "FIVE": 5,
        "TEN": 10,
        "TWENTY": 20,
        "ONE HUNDRED": 100
    }

    let sorted_cid = [...cid]
    sorted_cid.sort((x) => -money_value[x[0]])

    let total = 0;
    let status;
    let temp_change = original_change
    for (let item of sorted_cid) {
        if (item[1] > 0) {
            if (temp_change >= money_value[item[0]]) {
                quotient = Math.floor(temp_change / money_value[item[0]]);
                if (quotient <= item[1] / money_value[item[0]]) {
                    temp_change = (temp_change - quotient * money_value[item[0]]).toFixed(2);
                    change_array.push([item[0], quotient * money_value[item[0]]]);
                } else {
                    temp_change = (temp_change - item[1]).toFixed(2)
                    change_array.push([item[0], item[1]])
                }
            }
        }
        total += item[1];
    }

    if (total == original_change) {
        status = "CLOSED";
        change_array = [...cid]
    } else if (total < original_change || temp_change != 0) {
        status = "INSUFFICIENT_FUNDS";
        change_array = []
    } else {
        status = "OPEN"
    }

    let return_value = { "status": status, "change": change_array }
    return return_value
}

